let variabelNum = 10;
let variabelNum2 = 10;
let variabelNum3 = 5;

let variabelString = "10";

console.log(variabelNum > variabelNum3); // true
console.log(variabelNum < variabelNum3); // false
console.log(variabelNum >= variabelNum2); // true
console.log(variabelNum <= variabelNum3); // false
console.log(variabelNum == variabelString); // true -> nilai sama aja tipe data beda gapapa
console.log(variabelNum === variabelNum3); // false -> nilai dan tipe data harus sama
console.log(variabelNum != variabelString); // false
console.log(variabelNum !== variabelString); // true
